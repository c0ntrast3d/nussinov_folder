package it.unicam.cs.asdl1819.project1;

public class Cell {

    private final int left;
    private final int right;

    public Cell(int left, int right) {
        this.left = left;
        this.right = right;
    }

    public int getLeft() {
        return left;
    }

    public int getRight() {
        return right;
    }


}